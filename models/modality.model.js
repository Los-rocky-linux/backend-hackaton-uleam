const mongoose = require("mongoose");
const { Schema } = mongoose;

const ModalitySchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
    versionKey: false,
  }
);

module.exports = mongoose.model("Modality", ModalitySchema);
